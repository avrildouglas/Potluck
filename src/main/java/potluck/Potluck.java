package potluck;

public class Potluck {
	
	private Long plId;
	private String name;
	private String food;
	
	
	public Long getPlId() {
		return plId;
	}
	
	public void setPlId(Long plId) {
		this.plId = plId;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getFood() {
		return food;
	}
	
	public void setFood(String food) {
		this.food = food;
	}

}
